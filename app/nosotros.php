		<h3>Información personal</h3>

		El equipo recopila cierta información personal de los usuarios que acceden a nuestras aplicaciones, a fin de realizar consultas y trámites on-line. Dichos usuarios necesitan encontrarse previamente registrados. Al respecto, se aclara que:
		<ul>
			<li>La información que se obtiene por parte de sus socios es de carácter estrictamente confidencial.</li>
			<li>La información de carácter económico y comercial de los socios también reviste el carácter de confidencial, por lo que su tratamiento deberá ser utilizado conforme a la presente política y las leyes vigentes.</li>
			<li>Los empleados y asesores de los socios podrán utilizar la información confidencial de los socios exclusivamente durante el desempeño de las tareas habituales de la empresa, encontrándose prohibido hacer uso de la misma fuera del ámbito laboral o con propósitos distintos a los necesarios para su desempeño.</li>
			<li>Toda documentación que contenga información confidencial de un socio, cualquiera sea el soporte en que se encuentre deberá ser tratada con cuidado y diligencia.</li>
			<li>Se encuentra rigurosamente prohibido brindar acceso a información de  socios a terceros ajenos a la empresa, con la única excepción de aquellas solicitudes efectuadas por autoridad competente, parlamentaria, administrativa o judicial. En ningún caso se le podrá brindar información a un socio relacionada a otro socio que no pertenezca a su grupo familiar.</li>
			<li>En caso que un socio solicite información confidencial propia, deberá previamente constatarse la identidad del requirente, solicitándole que informe su nombre, domicilio, RUT, composición del grupo familiar, fecha de vencimiento de su factura y cualquier otra pregunta que el personal considere necesaria a los efectos de constatar su identidad.</li>
